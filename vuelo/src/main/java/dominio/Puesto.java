package dominio;

public class Puesto {
	
	private Integer numero;
	private String tipo;
	
	public void setNumero(Integer numero) {
		this.numero = numero;
	}
	
	public String getTipo() {
		return tipo;
	}

	public void setTipo(String tipo) {
		this.tipo = tipo;
	}

	public Integer getNumero() {
		return numero;
	}

}
